export const getInitialDate = (date: Date) => {
  let day = String(date.getDate()).padStart(2, '0');
  const month = String(date.getMonth() + 1).padStart(2, '0');
  const year = date.getFullYear();

  if (Number(day) > 1) {
    day = "01"
  }

  return `${year}-${month}-${day}`;
};

